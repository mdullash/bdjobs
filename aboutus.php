<p>
Bdjobs.com Ltd. is the first and leading career management site in the country. <br></br> Eight young business and IT professional backed by strong command over e-business and in-depth understanding of the needs of job seekers and employers in the country's context started this venture on July 2000.
</p>

<h3>The vision of the company is to try bringing Internet technology in the mainstream business and economic life of the society.</h3>

<p>
Our web-site aims to explore maximum benefits of the Internet. We believe our service will help the job seekers manage their career more efficiently. This site will also help employers solve many of the problems associated with traditional recruiting methods and allow them to save time and money.

Right after its launching, the site has been able to attract the Internet users in the country. The site regularly updates Job Information (on average more than 2500 valid job news are placed at any point of time at the site), provides facility to the job seekers for posting resume and online application. The site has also been able to get good response from a large number of organizations in the country who use online job advertisement facility, online CV bank access and online application receiving and processing facility of www.bdjobs.com. Till now, more than 12,000 employers in the country have recruited more than 3,50,000 professionals at different levels for their organizations through Bdjobs.com service
</p>



